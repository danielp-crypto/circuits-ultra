    function changeQty(id, delta) {
      let input = document.getElementById(id);
      let value = parseInt(input.value) || 1;
      value += delta;
      if (value < 1) value = 1;
      input.value = value;
    }

    function addToCart(product, price, size, qty) {
      if (!size) {
        alert("Please select a size before adding to cart.");
        return;
      }
      qty = parseInt(qty);
      let item = cart.find(i => i.product === product && i.size === size);
      if (item) {
        item.quantity += qty;
      } else {
        cart.push({ product, price, size, quantity: qty });
      }
      updateCart();
    }

    function removeFromCart(index) {
      cart.splice(index, 1);
      updateCart();
    }

    function updateCart() {
  let cartList = document.getElementById('cart');
  let totalDisplay = document.getElementById('total');
  let checkoutAmount = document.getElementById('checkout-amount');
  let checkoutItem = document.getElementById('checkout-item');
  
  cartList.innerHTML = '';
  total = 0;

  cart.forEach((item, index) => {
    total += item.price * item.quantity;
    let li = document.createElement('li');
    li.className = "list-group-item d-flex justify-content-between align-items-center";

    li.innerHTML = `
      <div>
        ${item.product} (${item.size}) - R${item.price} x ${item.quantity}
      </div>
      <button class="btn btn-sm btn-danger" onclick="removeFromCart(${index})">Remove</button>
    `;
    cartList.appendChild(li);
  });

  totalDisplay.textContent = total;

  // Update PayFast form
  checkoutAmount.value = total.toFixed(2);
  checkoutItem.value = cart.map(i => i.product + " x" + i.quantity).join(", ") || "Cart Purchase";
}

